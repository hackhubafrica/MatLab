# Example parameters for a mechanical rotational system
J = 0.5    # Moment of inertia (kg.m^2)
B = 0.1    # Damping coefficient (N.m.s/rad)
K = 2.0    # Spring constant (N.m/rad)

# Print the parameters
print(f"Moment of Inertia (J): {J} kg.m^2")
print(f"Damping Coefficient (B): {B} N.m.s/rad")
print(f"Spring Constant (K): {K} N.m/rad")

'''
Explanation:
Parameters J, B, and K represent the moment of inertia, damping coefficient, and spring constant, respectively, for a mechanical rotational system.
These parameters are fundamental for constructing the equations of motion (e.g., using Newton's second law for rotational systems).
'''